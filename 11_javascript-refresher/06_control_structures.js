//IF, ELSE IF, ELS
let score = 99; 

if (score >= 90) {   
    console.log("Grade: A")
}
else if (score >= 80) {
    console.log("Grade: B")
} 
else if (score >= 70) {
    console.log("Grade: C")
}
else {
    console.log("Grade: F")
}   

//FOR LOOP
for (let i = 1; i <= 5; i++) {
    console.log(i);
}
//WHILE LOOP
let count = 0;      
while (count < 3) {
    console.log("Hello");
    count++;
}

